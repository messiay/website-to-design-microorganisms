def generate_roadmap(organism, gene, expression_level):
 # Simulating genetic engineering algorithm for demo purposes
 roadmap = f"""
1. Obtain {organism} cells
2. Design {gene} construct for {expression_level}% expression
3. Transfect cells with construct
4. Select and culture transfected cells
5. Verify {gene} expression at {expression_level}%
 """
 return roadmap
